
class TouchPuzzleData:
    def __init__(self, 
                  id,
                  name, 
                  piece_height_pixels, 
                  piece_width_pixels,
                  height_pieces,
                  width_pieces):
        self.id = id
        self.name = name
        self.piece_height_pixels = piece_height_pixels
        self.piece_width_pixels = piece_width_pixels
        self.height_pieces = height_pieces
        self.width_pieces = width_pieces